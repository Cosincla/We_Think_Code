#include "libft.h"

char	*ft_backws(char *str, int i)
{
	while ((str[i] == ' ' || str[i] == '\t' || str[i] == '\v' ||
				str[i] == '\n' || str[i] == '\f') && str[i] >= 0)
		i--;
	while (str[i] != ' ' && str[i] != '\t' && str[i] != '\v' &&
			str[i] != '\n' && str[i] != '\f' && str[i] >= 0)
		i--;
	i++;
	return (str + i);
}
