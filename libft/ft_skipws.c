#include "libft.h"

char	*ft_skipws(char *str, int i)
{
	while ((str[i] == ' ' || str[i] == '\t' || str[i] == '\n' ||
				str[i] == '\f' || str[i] == '\v') && str[i] != '\0')
		i++;
	if (str[i] == '\0')
		return (NULL);
	return (str + i);
}
